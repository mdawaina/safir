function getSystemLanguage() {
    var sysLang = "en";
    var systemLang = navigator.language || navigator.browserLanguage || ""; 
    if (systemLang.indexOf("ar") > -1) {
        sysLang = "ar";

    } else {
        sysLang = "en";
        
    }

    return sysLang;
}

